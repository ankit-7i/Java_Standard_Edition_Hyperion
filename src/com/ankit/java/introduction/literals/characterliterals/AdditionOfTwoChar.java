package com.ankit.java.introduction.literals.characterliterals;

public class AdditionOfTwoChar {
    public static void main(String[] args) {
        int x = 'A';
        int y = 'B';
        System.out.println(x+y);
        System.out.println('a'+'a');
        System.out.println("a"+"a");
    }
}
